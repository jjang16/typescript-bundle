//
//  Much of this project is built and tested with the
//  example project in the ./spec/project/in directory.
//  The general workflow for making edits to this
//  codebase involve running the watch-spec command
//  that will start a watch on the codebase followed
//  by compiling the ./spec/project/in/tsconfig.json
//  project.
//
//  $ npm run watch-spec
//
//  A subsequent task can be run in another terminal
//  to start another watcher on the output project. 
//  Any edits made to TypeScript-Bundle will result
//  in the output project being re-run.
//
//  $ npm run watch-spec-out
//
// ------------------------------------------------------